                    <h2>1. Çerezler Nedir?</h2>
                    <p>Çerezler (cookies), bir web sitesini ziyaret ettiğinizde tarayıcınız aracılığıyla cihazınıza kaydedilen küçük metin dosyalarıdır. Bu dosyalar, web sitesinin düzgün çalışması, kullanıcı deneyiminin iyileştirilmesi, site kullanımının analiz edilmesi ve kişiselleştirilmiş içerik sunulması gibi amaçlar için kullanılmaktadır.</p>
