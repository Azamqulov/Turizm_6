                    <h2>2. Kullandığımız Çerez Türleri</h2>
                    <p>Web sitemizde aşağıdaki çerez türlerini kullanmaktayız:</p>
                    
                    <h3>2.1. Zorunlu Çerezler</h3>
                    <p>Bu çerezler, web sitesinin temel işlevlerini gerçekleştirmek için gereklidir ve devre dışı bırakılamazlar. Bunlar genellikle yalnızca sizin tarafınızdan yapılan ve gizlilik tercihlerinizi ayarlama, oturum açma veya form doldurma gibi hizmet taleplerine yanıt olarak ayarlanır.</p>
                    
                    <h3>2.2. Performans Çerezleri</h3>
                    <p>Bu çerezler, ziyaretçilerin web sitemizi nasıl kullandığı hakkında bilgi toplar. Bu çerezler, hangi sayfaların en popüler olduğunu, kullanıcıların sitede nasıl gezindiğini ve karşılaştıkları hata mesajlarını belirlememize yardımcı olur. Bu bilgiler, web sitemizin performansını iyileştirmemize yardımcı olur.</p>
                    
                    <h3>2.3. İşlevsellik Çerezleri</h3>
                    <p>Bu çerezler, web sitemizin dil tercihi veya bölge seçimi gibi yaptığınız seçimleri hatırlamasını sağlar. Bu çerezler sayesinde size daha kişiselleştirilmiş bir deneyim sunabiliriz.</p>
                    
                    <h3>2.4. Hedefleme/Reklam Çerezleri</h3>
                    <p>Bu çerezler, ilgi alanlarınıza göre size özel reklamlar göstermek için kullanılır. Ayrıca, bir reklamın görüntülenme sayısını sınırlamak ve reklam kampanyalarının etkinliğini ölçmek için de kullanılırlar.</p>
